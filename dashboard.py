def _am(dec):
    dec = float(dec)
    return f"+{round((dec-1)*100)}" if dec >= 2 else f"{round(-100/(dec-1))}"


def _short(n):
    p = (n or "").split()
    return f"{p[0][0]}. {p[-1]}" if len(p) >= 2 else (n or "")


def _conf(r):
    """Confidence = how reliably the projection clears the line: hit rate shrunk toward 50%
    by sample size (odds-INDEPENDENT — price is value, not confidence)."""
    ph, n = r["proj_hit"], r["n_elev"]
    if ph is None or not n:
        return 0.0
    return (ph * n + 0.5 * 6) / (n + 6)


def _units(ph, n, dec):
    """Quarter-Kelly recommended stake in units (see wnba_tonight.kelly_units)."""
    if ph is None or not n or dec <= 1:
        return 1.0
    p = (ph * n + (1.0 / dec) * 6) / (n + 6)
    f = (p * dec - 1) / (dec - 1)
    if f <= 0:
        return 0.5
    return max(0.5, min(3.0, round((0.25 * f / 0.04) * 2) / 2))


def _tip_times():
    """{team_abbr: tip_datetime_utc} for today's slate from ESPN, so cards can be ordered
    by game time and show it."""
    et = dt.datetime.now(dt.timezone.utc).astimezone(dt.timezone(dt.timedelta(hours=-4))).strftime("%Y%m%d")
    try:
        j = requests.get(
            f"https://site.api.espn.com/apis/site/v2/sports/basketball/wnba/scoreboard?dates={et}",
            headers={"User-Agent": "Mozilla/5.0"}, timeout=15).json()
    except requests.RequestException:
        return {}
    out = {}
    for e in j.get("events", []):
        try:
            tip = dt.datetime.fromisoformat((e.get("date") or "").replace("Z", "+00:00"))
        except ValueError:
            continue
        for c in e.get("competitions", [{}])[0].get("competitors", []):
            ab = c.get("team", {}).get("abbreviation")
            if ab:
                out[ab] = tip
    return out


def _load(mt_date):
    if not LEDGER.exists():
        return [], (0, 0, 0.0, 0)
    con = sqlite3.connect(LEDGER)
    con.row_factory = sqlite3.Row
    rows = [dict(r) for r in con.execute(
        "SELECT * FROM predictions WHERE pred_date=? ORDER BY ev DESC", (mt_date,))]
    g = con.execute("SELECT result,odds FROM predictions WHERE graded=1 "
                    "AND pred_date>='2026-07-09'").fetchall()
    con.close()
    dec = [r for r in g if r[0] in ("over", "under")]
    w = sum(1 for r in dec if r[0] == "over")
    u = sum((r[1] - 1) if r[0] == "over" else -1 for r in dec)
    pend = sum(1 for r in rows if r["result"] is None)
    return rows, (w, len(dec) - w, u, pend)


def _bars(r):
    """PropsCash-style game log: vertical bars (oldest->newest), the line drawn across,
    each game green if over / red if under, opponent under each bar."""
    try:
        s = json.loads(r["samples"]) if r["samples"] else []
    except (ValueError, TypeError):
        s = []
    if not s:
        return '<div class="nodata">no game data</div>'
    s = list(reversed(s))                              # stored newest-first -> show oldest-left
    line = float(r["line"])
    vals = [x[0] for x in s]
    mx = max(max(vals), line) * 1.14 or 1
    over = sum(1 for v in vals if v > line)
    note = ("elevated-role games" if r["basis"] == "elevated"
            else f"projected to ~{r['proj_min']:.0f} min")
    # bars (baseline = chart bottom) and opponent labels are SEPARATE rows, so the bars and
    # the prop line share one baseline + scale.
    bars = "".join(
        f'<div class="col"><div class="b {"o" if v > line else "u"}" style="height:{v/mx*100:.1f}%">'
        f'<span class="bv">{v:g}</span></div></div>' for v, *_ in s)
    opps = "".join(f'<span>{html.escape(str(o) or "")}</span>' for _, o, *_ in s)
    return (f'<div class="bars"><div class="chart">'
            f'<div class="pline" style="bottom:{line/mx*100:.1f}%"><span>{line:g}</span></div>'
            f'{bars}</div><div class="opps">{opps}</div>'
            f'<div class="bnote">{over}/{len(s)} over {line:g} · {note}</div></div>')


def _mkt_row(r):
    stat = STAT.get(r["stat"], r["stat"].upper())
    res, actual = r["result"], r["actual"]
    if res == "over":
        badge, cls = f"HIT {actual:g}", "hit"
    elif res == "under":
        badge, cls = f"miss {actual:g}", "miss"
    elif res == "push":
        badge, cls = f"push {actual:g}", "pend"
    else:
        badge, cls = "•", "pend"
    proj, n, ph = r["elev_avg"], r["n_elev"], r["proj_hit"]
    if ph is not None and n:
        overs = round(ph * n)
        mid = f'proj {proj:g} · <b>{overs}-{n-overs}</b> ({ph*100:.0f}%)'
    else:
        mid = f'proj {proj:g}'
    u = _units(r["proj_hit"], r["n_elev"], float(r["odds"]))
    return f"""
      <div class="mkt {cls}" onclick="this.nextElementSibling.classList.toggle('open')">
        <div class="mline"><span class="ms">{stat}</span> o{r['line']:g}
          <span class="mo">{_am(r['odds'])}</span> <span class="mu">{u:g}u</span></div>
        <div class="mmid">{mid}</div>
        <div class="mbadge {cls}">{html.escape(badge)}</div>
        <div class="chev">›</div>
      </div>{_bars(r)}"""


def _player_card(player, rows, tip=None):
    outs = ", ".join(sorted({_short(r["out_player"]) for r in rows}))
    r0 = rows[0]
    team = (r0.get("team") or "").upper()
    logo = (f'<img class="logo" src="{LOGO.format(team.lower())}" alt="" '
            f'onerror="this.style.display=\'none\'">' if team else "")
    when = tip.astimezone(MT).strftime("%-I:%M %p") if tip else ""
    game = " · ".join(x for x in (f"{team} vs {r0['opp']}" if r0.get("opp") else team,
                                  f"{when} MT" if when else "") if x)
    cb = {"confirmed": ("✓ starting", "cok"), "bench": ("not starting", "cbad"),
          "likely": ("likely starts", "cmid"), "projected": ("lineup TBD", "cmid")}.get(
              r0.get("confidence") or "projected", ("", ""))
    conf_html = f'<span class="cconf {cb[1]}">{cb[0]}</span>' if cb[0] else ""
    ctx = []
    if r0.get("proj_min"):
        dm = f" ({r0['d_min']:+g})" if r0.get("d_min") is not None else ""
        ctx.append(f"~{r0['proj_min']:.0f} min{dm}")
    if r0.get("total"):
        ctx.append(f"O/U {r0['total']:g}")
    if r0.get("opp_def"):
        ctx.append(f"opp allows {r0['opp_def']:g}")
    mkts = "".join(_mkt_row(r) for r in sorted(rows, key=lambda r: -_conf(r)))
    return f"""
    <div class="card">
      <div class="chead">
        <div class="cname">{logo}{html.escape(_short(player))}</div>
        <div class="ctag">{html.escape(outs)} out</div>
      </div>
      <div class="cgame">{html.escape(game)}{conf_html}</div>
      <div class="cctx">{' · '.join(html.escape(c) for c in ctx)}</div>
      {mkts}
    </div>"""


def _load_tt():
    f = HERE / "tt_board.json"
    if not f.exists():
        return None
    try:
        return json.loads(f.read_text())
    except (ValueError, OSError):
        return None


def _tracker_panel(wnba_rec, tt_json):
    """Tracker tab — live record / hit rate / units for WNBA and Table Tennis, flat 1u,
    updated as each event settles."""
    def card(emoji, title, w, l, u, note):
        n = w + l
        hit = f"{w/n*100:.0f}%" if n else "—"
        roi = f"{u/n*100:+.0f}%" if n else ""
        ucls = "up" if u > 0 else ("down" if u < 0 else "")
        return f"""
      <div class="tcard">
        <div class="thead">{emoji} {html.escape(title)}</div>
        <div class="trow">
          <div class="tbox"><div class="tk">Record</div><div class="tv">{w}-{l}</div></div>
          <div class="tbox"><div class="tk">Hit rate</div><div class="tv">{hit}</div></div>
          <div class="tbox"><div class="tk">Units</div><div class="tv {ucls}">{u:+.1f}u</div></div>
        </div>
        <div class="tsub">{html.escape(note)}{(' · ROI ' + roi) if roi else ''}</div>
      </div>"""
    w, l, u = wnba_rec
    out = card("🏀", "WNBA injury props", w, l, u, "flat 1u · settles live · since 7/9")
    tt = (tt_json or {}).get("tracker")
    if tt:
        out += card("🏓", "Table tennis", tt.get("w", 0), tt.get("l", 0), tt.get("u", 0.0),
                    "flat 1u · settles live · since 7/9")
    else:
        out += ('<div class="tcard"><div class="thead">🏓 Table tennis</div>'
                '<div class="tsub">connecting… (needs the tt-elite bridge)</div></div>')
    return out


def _tt_ladder(lad, model_line, zone):
    """Tap-to-expand hit-rate ladder: for a matchup, the raw H2H over/under rate at every
    line a book might post (70.5–80.5), so the user can read the rate at THEIR exact line,
    not just the model's 74.5. Split bar = over(green)/under(red) balance; record shows the
    sample. Over% is monotone down the ladder."""
    if not lad:
        return '<div class="ttlad"><div class="ladnote">no H2H line ladder</div></div>'
    over_side = zone.startswith("O")
    rows = ""
    for r in lad:
        op = r["op"]
        up = 100 - op
        mk = " mark" if abs(r["line"] - model_line) < 0.01 else ""
        lbl = f'{r["line"]:g}' + (' <span class="ladm">◄</span>' if mk else "")
        # emphasize the model's flagged side's number
        ocls = "lo hi" if over_side else "lo"
        ucls = "lu hi" if not over_side else "lu"
        rows += (f'<div class="ladrow{mk}">'
                 f'<span class="ladl">{lbl}</span>'
                 f'<span class="ladbar"><i class="lo" style="width:{op}%"></i>'
                 f'<i class="lu" style="width:{up}%"></i></span>'
                 f'<span class="ladv"><b class="{ocls}">{op}</b>'
                 f'<span class="lsl">/</span><b class="{ucls}">{up}</b></span>'
                 f'<span class="ladr">{r["o"]}-{r["u"]}</span></div>')
    return (f'<div class="ttlad">'
            f'<div class="ladhead"><span>Line</span><span>over / under</span>'
            f'<span class="ladv">O% / U%</span><span class="ladr">rec</span></div>'
            f'{rows}'
            f'<div class="ladnote">raw H2H hit rate per line · ◄ model line · pick your book\'s line</div></div>')


def _tt_panel(data):
    """Table Tennis tab — today's flagged bets, grouped by league, ordered by game time.
    Each matchup taps open to a per-line hit-rate ladder."""
    if not data:
        return ('<div class="empty">Table tennis feed connecting…<br>'
                '<span>Once the tt-elite bridge is set up, today\'s TT bets show here.</span></div>')
    bets = data.get("bets", [])
    if not bets:
        return ('<div class="empty">No TT bets flagged right now.<br>'
                '<span>Fixtures post a few hours before play — check back closer to the slate.</span></div>')
    model_line = data.get("model_line", 74.5)
    by = defaultdict(list)
    for b in bets:
        by[b["league"]].append(b)
    out = []
    for league in sorted(by):
        games = sorted(by[league], key=lambda b: b.get("ts") or 0)
        cards = ""
        for b in games:
            when = (dt.datetime.fromtimestamp(b["ts"], MT).strftime("%-I:%M %p")
                    if b.get("ts") else "TBD")
            side = html.escape(b.get("side", ""))
            lad = _tt_ladder(b.get("ladder", []), model_line, b.get("side", ""))
            cards += f"""
      <div class="ttrow" onclick="this.nextElementSibling.classList.toggle('open')">
        <div class="ttmain"><b>{html.escape(b['p1'])}</b> v {html.escape(b['p2'])}<span class="ttchev">›</span></div>
        <div class="ttsub">{when} MT · <span class="ttside">{side}</span> · {b['rec']} ({b['raw']}%) · {b['u']:g}u</div>
      </div>{lad}"""
        out.append(f'<div class="card"><h3 class="ttlg">{html.escape(league)}</h3>{cards}</div>')
    return "\n".join(out)


