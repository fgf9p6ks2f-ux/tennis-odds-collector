import html
import json
import sqlite3
from collections import defaultdict
from pathlib import Path

import requests

try:
    from zoneinfo import ZoneInfo
    MT = ZoneInfo("America/Denver")            # user-local, for DISPLAY times only
    ET = ZoneInfo("America/New_York")          # the slate day + pred_date basis, for DATE filters
except Exception:
    MT = dt.timezone(dt.timedelta(hours=-6))
    ET = dt.timezone(dt.timedelta(hours=-4))

HERE = Path(__file__).resolve().parent
FRESH_MIN = 90     # best-price: only quotes re-posted within this many min of the newest count as live
LEDGER = HERE / "wnba_ledger.sqlite"
OUT = HERE / "docs" / "index.html"
STAT = {"points": "PTS", "rebounds": "REB", "assists": "AST",
        "pra": "PRA", "pts_reb": "P+R", "pts_ast": "P+A", "reb_ast": "R+A"}
STATKEY = {"points": "pts", "rebounds": "reb", "assists": "ast",
           "pra": "pra", "pts_reb": "pts_reb", "pts_ast": "pts_ast", "reb_ast": "reb_ast"}
THIN_EV = 0.35
LOGO = "https://a.espncdn.com/i/teamlogos/wnba/500/{}.png"

# ---- LIVE bar recompute ----------------------------------------------------------------------
# The dropdown bars are recomputed FRESH on every dashboard build from current game logs + the
# current out-set — so a change to bar logic (or a game just played) shows up on the next rebuild
# instead of waiting for the alert to re-log the frozen `samples` snapshot. Falls back to the stored
# samples on any lookup/fetch failure, so the dashboard never hard-depends on the network.
_GLOG_TTL = 1200                                   # 20 min: a 60s dashboard loop won't refetch each build
_GLOG_FILE = HERE / "wnba_gamelog_cache.json"
_glog_mem = {}
_glog_disk = None


def _name2id():
    try:
        d = json.loads((HERE / "wnba_players_cache.json").read_text())
        return {n: v.get("id") for n, v in d.get("players", {}).items() if v.get("id")}
    except (OSError, ValueError):
        return {}


NAME2ID = _name2id()


def _glog(pid):
    """A player's game log, cached in-process + on disk with a TTL. Returns the stale copy (or [])
    if the fetch fails, so the dashboard degrades to stored samples instead of breaking."""
    global _glog_disk
    if pid in _glog_mem:
        return _glog_mem[pid]
    if _glog_disk is None:
        try:
            _glog_disk = json.loads(_GLOG_FILE.read_text())
        except (OSError, ValueError):
            _glog_disk = {}
    now = dt.datetime.now(dt.timezone.utc).timestamp()
    ent = _glog_disk.get(pid)
    if ent and now - ent.get("ts", 0) < _GLOG_TTL:
        _glog_mem[pid] = ent["log"]
        return ent["log"]
    try:
        import wnba_wowy as W                       # lazy: keeps dashboard import light + numpy-free
        log = W.game_log(pid)
    except Exception:
        log = ent["log"] if ent else []
    _glog_disk[pid] = {"ts": now, "log": log}
    _glog_mem[pid] = log
    try:
        _GLOG_FILE.write_text(json.dumps(_glog_disk))
    except OSError:
        pass
    return log


def _fresh_bars(r):
    """Recompute the same-injury-context bars live: the player's games where tonight's ENTIRE out-set
    was absent, newest-first (up to 10). Returns None to fall back to the stored snapshot (unknown id,
    no logs, or <2 same-context games — the same floor the logger uses)."""
    try:
        pid = NAME2ID.get(r["player"])
        key = STATKEY.get(r["stat"])
        if not pid or not key:
            return None
        blog = [g for g in _glog(pid) if (g.get("min") or 0) > 0]
        out_sets = []
        for nm in (r.get("out_player") or "").split(","):
            opid = NAME2ID.get(nm.strip())
            if opid:
                out_sets.append({g["date"][:10] for g in _glog(opid) if (g.get("min") or 0) > 0})
        if not out_sets:
            return None
        same = [g for g in blog if all(g["date"][:10] not in s for s in out_sets)]
        if len(same) < 2:
            return None
        recent = sorted(same, key=lambda g: g["date"], reverse=True)[:10]
        return [[round(g.get(key, 0)), g.get("matchup", ""), round(g.get("min", 0))] for g in recent]
    except Exception:
        return None


def _samples(r):
    """Bar samples for a row — recomputed live when possible, else the stored snapshot. Cached on the
    row so _bars and _raw_record agree and don't double-fetch."""
    if "_barcache" not in r:
        s = _fresh_bars(r)
        if s is None:
            try:
                s = json.loads(r["samples"]) if r["samples"] else []
            except (ValueError, TypeError):
                s = []
        r["_barcache"] = s
    return r["_barcache"]


def _am(dec):
    dec = float(dec)
    return f"+{round((dec-1)*100)}" if dec >= 2 else f"{round(-100/(dec-1))}"


def _short(n):
    p = (n or "").split()
    return f"{p[0][0]}. {p[-1]}" if len(p) >= 2 else (n or "")


def _conf(r):
    """Confidence = how reliably the projection clears the line: hit rate shrunk toward 50%
    by sample size (odds-INDEPENDENT — price is value, not confidence)."""
    ph, n = r["proj_hit"], r["n_elev"]
    if ph is None or not n:
        return 0.0
    return (ph * n + 0.5 * 6) / (n + 6)


def _units(dec):
    """Recommended stake by ODDS (risk control), NOT model edge — calibrated to the user's own
    ladder staking: full 1u at the anchor (~-110 through +100), then decay hard up the ladder so
    you never overexpose on one player. ~0.5u @ +170, 0.4u @ +200, 0.25u @ +250, 0.2u @ +300,
    0.15u floor for the deep +500/+600 lottery rungs."""
    dec = float(dec)
    if dec <= 2.05:                             # -inf .. ~+105 (favorites through pick'em): base
        return 1.0
    return max(0.15, round((2.0 / dec) ** 2.4 / 0.05) * 0.05)


def _tip_times():
    """{(slate_date_iso, team_abbr): tip_datetime_utc} for today's AND tomorrow's slates from
    ESPN, so cards order by game time (board is tip-sorted) and show it. Keyed by date because
    back-to-backs put the same team on both slates."""
    et_now = dt.datetime.now(dt.timezone.utc).astimezone(dt.timezone(dt.timedelta(hours=-4)))
    out = {}
    for day in (et_now, et_now + dt.timedelta(days=1)):
        try:
            j = requests.get(
                "https://site.api.espn.com/apis/site/v2/sports/basketball/wnba/scoreboard"
                f"?dates={day:%Y%m%d}",
                headers={"User-Agent": "Mozilla/5.0"}, timeout=15).json()
        except requests.RequestException:
            continue
        for e in j.get("events", []):
            try:
                tip = dt.datetime.fromisoformat((e.get("date") or "").replace("Z", "+00:00"))
            except ValueError:
                continue
            for c in e.get("competitions", [{}])[0].get("competitors", []):
                ab = c.get("team", {}).get("abbreviation")
                if ab:
                    out[(day.strftime("%Y-%m-%d"), ab)] = tip
    return out


def _load(mt_date):
    if not LEDGER.exists():
        return [], (0, 0, 0.0, 0)
    con = sqlite3.connect(LEDGER)
    con.row_factory = sqlite3.Row
    # BOARD shows every un-settled bet from today ONWARD — a next-day play appears the moment
    # it's written (not when its calendar date arrives); a bet drops off the moment it grades
    # (its result then lives in the Tracker record, below).
    rows = [dict(r) for r in con.execute(
        "SELECT * FROM predictions WHERE pred_date>=? AND result IS NULL "
        "ORDER BY pred_date ASC, ev DESC",
        (mt_date,))]
    g = [dict(r) for r in con.execute("SELECT result,odds,side,pred_date,player,team,stat,line,n_elev,d_min,ev,elev_avg "
                                      "FROM predictions WHERE graded=1 AND pred_date>='2026-07-09'")]
    con.close()
    # durable user-played marks (wnba_played.txt) — read-only, so the ✓ shows on the board
    # independent of when CI last synced the DB column
    pf = HERE / "wnba_played.txt"
    if pf.exists():
        marks = [ln.split("|") for ln in pf.read_text().splitlines() if ln.count("|") == 3]
        for r in rows:
            if any(d == r["pred_date"] and s == r["stat"] and pl.lower() in (r["player"] or "").lower()
                   and abs((r["line"] or 0) - float(ln)) < 1e-9 for d, pl, s, ln in marks):
                r["played"] = 1
    # CURRENT-MODEL record (2026-07-14): overs-only, AND restated to what the current bot would PICK —
    # wnba_slip.current_selection drops the thin-sample over-extrapolations the guard now skips (Ayayi)
    # and the correlated over-stacks (Copper's points+pts_reb+pra -> keep best only). Rule-based +
    # outcome-blind (it also drops a lucky thin-sample WIN), so it's the new bot's selection, not
    # cherry-picking. The ledger keeps every real bet; this only restates the headline record.
    overs = [r for r in g if r["result"] in ("over", "under") and (r["side"] or "over") == "over"]
    try:
        import wnba_slip as _SL
        dec, _dropped = _SL.current_selection(overs)
    except Exception:
        dec = overs
    w = sum(1 for r in dec if r["result"] == (r["side"] or "over"))   # win = result matches side
    # P&L under the user's LADDER STAKING (2026-07-14): 1u on the base line, declining rungs up the
    # ladder (0.5 / 0.25 / 0.25 ...), capped 2.5u per player-stat ladder — his real sizing + the
    # exposure cap, so a fully-laddered cold game can't run past 2.5u on one stat.
    try:
        import wnba_slip as SLIP
        sm = SLIP.ladder_stake_map(dec)
        def _stk(r):
            return sm.get((r["pred_date"], r["player"], r["stat"], r["line"]), 1.0)
    except Exception:
        def _stk(r):
            return 1.0
    u = sum(_stk(r) * (r["odds"] - 1) if r["result"] == (r["side"] or "over") else -_stk(r) for r in dec)
    return rows, (w, len(dec) - w, u, len(rows))


def _raw_record(r):
    """Hit count on the ACTUAL game values shown in the chart — NOT the minutes-honest scaled
    proj_hit. This is what really happened, so the reasoning, the record, and the bars all agree
    on one number. Also returns how many of the last 3 (most recent) went OVER, to surface a
    role that's climbing faster than the flat average shows."""
    s = _samples(r)
    if not s:
        return None
    line = float(r["line"])
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"
    on = (lambda v: v > line) if side == "over" else (lambda v: v < line)
    hits = sum(1 for x in s if on(x[0]))
    recent_over = sum(1 for x in s[:3] if x[0] > line)      # samples are stored newest-first
    return hits, len(s), side, recent_over


def _reasoning(r):
    """A generated, model-grounded 'why the model likes this' line for the dropdown, built from
    the logged features (projection, role change, hit record, stale line, lineup status)."""
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"
    stat = STAT.get(r["stat"], r["stat"])
    line = float(r["line"])
    proj = r["elev_avg"]
    # VOLUME-confirmed points over: a distinct rationale (project off sticky shot volume, not
    # recent points, which lag on shooting variance — the user's laddering edge).
    try:
        vol = json.loads(r["vol"]) if r.get("vol") else {}
    except (ValueError, TypeError):
        vol = {}
    if r.get("basis") == "volume" and vol:
        return (f"<b>Volume play.</b> FGA jumped <b>{vol['bf']:g}→{vol['rf']:g}</b> in the role — "
                f"projects <b>{vol['vp']:g} pts</b> off volume (sticky), not recent shooting (variance).")
    savg, ph, n = r.get("season_avg"), r.get("proj_hit"), r.get("n_elev")
    pm, dmin, drv = r.get("proj_min"), r.get("d_min"), r.get("driver")
    _on = [_short(nm.strip()) for nm in (r.get("out_player") or "").split(",") if nm.strip()]
    out = " + ".join(_on) if _on else None      # full out-set, not a mangled single name
    dl = {"points": "usage", "rebounds": "reb", "assists": "ast"}.get(r["stat"], "usg")
    role = ""
    if out:
        rb = []
        if dmin is not None and abs(dmin) >= 1:
            rb.append(f"{dmin:+.0f} min")
        if drv is not None and drv >= 1:
            rb.append(f"+{drv:.0f} {dl}")
        role = f"{out} out" + (f" ({', '.join(rb)})" if rb else "")
    if side == "under":
        b = [f"Projects <b>{proj:g} {stat}</b> under the {line:g} line" + (f" — {role}." if role else ".")]
    else:
        b = [f"Projects <b>{proj:g} {stat}</b> vs the {line:g} line" + (f" — {role}." if role else ".")]
    rec = _raw_record(r)
    if rec and rec[2] == "under" and rec[3] >= 2:
        b.append(f"⚠ she's gone OVER in {rec[3]} of her last 3 — treat this under with caution.")
    if r.get("stale") and savg is not None:
        b.append(f"Book anchored near the season avg ({savg:g}) — it hasn't repriced the role.")
    if r.get("confidence") == "bench":
        b.append("⚠ not in the projected starting five — minutes uncertain.")
    return " ".join(b)


def _regime_html(r):
    """Same-lineup comps block: the historical games that best reproduce tonight's FULL impact
    configuration — the ruled-out players absent AND the key teammates present — with the player's
    line in them and whether their average supports the bet. Conditioning on who's IN (not just who's
    out) is the point: usage is set by the whole lineup. A decision aid, not a projection change."""
    try:
        rg = json.loads(r["regime"]) if r.get("regime") else {}
    except (ValueError, TypeError):
        rg = {}
    if not rg or not rg.get("v2"):
        return ""                                     # pre-fix rows could claim outs who played
    if rg.get("no_comps"):
        nm = ", ".join(rg.get("sig_names") or [])
        return (f'<div class="regime"><div class="rgnone">⚡ First game without '
                f'<b>{html.escape(nm)}</b> — no history yet. That IS the edge: the book is '
                f'guessing too.</div></div>')
    if not rg.get("comps"):
        return ""
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"
    line = float(r["line"])
    stat = STAT.get(r["stat"], r["stat"])
    avg = rg["comp_avg"]
    names = ", ".join(rg.get("sig_names", [])[:3]) or "key players"
    # the IN-config that defines a TRUE comp — who's on the floor caps usage as much as who's out
    # (Johannes avg 13.7 ignoring this, but 9.5 once Ionescu/Stewart must be present as they are
    # tonight). Show it so the comp is legible: same lineup, not just same absences.
    inn = ", ".join(n.split()[-1] for n in rg.get("in_names", [])[:3])
    incfg = f' · <b>{html.escape(inn)}</b> in' if inn else ""
    np = rg.get("n_primary", rg.get("n_comps", 0))
    supports = (avg < line) if side == "under" else (avg > line)
    verdict = ("✓ " + side + " friendly") if supports else ("⚠ against the " + side)
    vcls = "sup" if supports else "warn"
    # mark the chips that match tonight's lineup BEST (the ones the avg is over) so it's obvious
    # which games are true same-lineup comps vs looser ones (a blowout where a starter also sat).
    bestm = max((c.get("match", 0) for c in rg["comps"]), default=0)
    chips = "".join(f'<span class="cmp{" pr" if c.get("match", 0) >= bestm - 0.08 else ""}">'
                    f'{html.escape(c["date"][5:])} '
                    f'{html.escape(str(c["opp"]))} <b>{c["val"]:g}</b> · {c["min"]:g}\'</span>'
                    for c in rg["comps"])
    div = ' · <span class="warn">tonight differs from her recent games</span>' if rg.get("divergent") else ""
    return (f'<div class="regime"><div class="rgh">Games with <b>{html.escape(names)}</b> actually out'
            f' — avg <b>{avg:g}</b> {stat} <span class="{vcls}">{verdict}</span>{div}</div>'
            + (f'<div class="rgsub">lineup match ranked by: {html.escape(inn)} playing</div>' if inn else '')
            + f'<div class="cmps">{chips}</div></div>')


def _bars(r):
    """Dropdown: generated reasoning + a PropsCash-style game log. Each game's ACTUAL stat is a
    bar (green if it cashed our side, red if not) with the line drawn across, and a gray MINUTES
    bar behind it (own 0-40 scale) so the role context is visible. Opponent under each bar."""
    why = f'<div class="why">{_reasoning(r)}</div>{_regime_html(r)}'
    s = _samples(r)
    if not s:
        return f'<div class="bars"><div class="bwrap">{why}<div class="nodata">no game data</div></div></div>'
    s = list(reversed(s))                              # newest-first -> show oldest-left
    line = float(r["line"])
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"
    vals = [x[0] for x in s]
    # TWO scales so the gray MINUTES bar is always clearly TALLER than the colored stat bar
    # (props.cash style): minutes fill toward the top on their own 0-42 range (a 30-min night ≈
    # 71%), stats are compressed into the lower ~62% so the minute number atop every bar stays
    # readable and never collides with the stat value.
    mx = max(max(vals), line) * 1.62 or 1
    onside = (lambda v: v > line) if side == "over" else (lambda v: v < line)
    hits = sum(1 for v in vals if onside(v))
    # the bars are the player's games matching tonight's injury context (out-set absent), so name it
    outs = ", ".join(_short(nm.strip()) for nm in (r.get("out_player") or "").split(",") if nm.strip())
    note = ("volume-confirmed role · bars = actual pts (shooting varies, volume doesn't)"
            if r["basis"] == "volume" else (f"games without {outs}" if outs else "elevated-role games")
            if r["basis"] == "elevated" else f"projected to ~{r['proj_min']:.0f} min")
    cols = ""
    for x in s:
        v = x[0]
        mn = x[2] if len(x) > 2 else 0                  # minutes that game (overlay, own 0-40 scale)
        cols += (f'<div class="col">'
                 f'<div class="m" style="height:{min(mn/42*100, 97):.0f}%"></div>'
                 f'<div class="b {"o" if onside(v) else "u"}" style="height:{v/mx*100:.1f}%">'
                 f'<span class="bv">{v:g}</span></div></div>')
    opps = "".join(f'<span>{html.escape(str(o) or "")}</span>' for _, o, *_ in s)
    return (f'<div class="bars"><div class="bwrap">{why}<div class="chart">'
            f'<div class="pline" style="bottom:{line/mx*100:.1f}%"><span>{line:g}</span></div>'
            f'{cols}</div><div class="opps">{opps}</div>'
            f'<div class="bnote">{hits}/{len(s)} {side} {line:g} · gray bar = minutes · {note}</div></div></div>')


def _splits(r):
    """The player's OVER/UNDER hit rate AT THIS LINE over recent windows — L5 / L10 / season / vs
    this opponent (H2H) — from the live game log. Returns {'l5':(hits,n), ...} or None. This is the
    props.cash time-split heatmap: raw recent form at the exact number, beside our ROLE edge."""
    pid = NAME2ID.get(r["player"])
    key = STATKEY.get(r["stat"])
    if not pid or not key:
        return None
    log = [g for g in _glog(pid) if (g.get("min") or 0) > 0]      # played games, newest-first
    if not log:
        return None
    line = float(r["line"])
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"

    def hr(games):
        vals = [g.get(key) for g in games if g.get(key) is not None]
        if not vals:
            return None
        return sum(1 for v in vals if (v > line) == (side == "over")), len(vals)

    opp = (r.get("opp") or "").upper()
    h2h = hr([g for g in log if (g.get("matchup") or "").upper() == opp]) if opp else None
    return {"l5": hr(log[:5]), "l10": hr(log[:10]), "szn": hr(log), "h2h": h2h}


def _hrcell(lbl, hr, role=False):
    """One heatmap cell: hit% at the line, W/N underneath. The TINT lives on the cell (faint green/
    red wash, desaturated value) — not glowing text — so the grid reads as a heatmap, and saturated
    green stays reserved for the model edge. `role` marks our injury-context cell (blue label)."""
    base = "gc r" if role else "gc"
    if not hr or not hr[1]:
        return (f'<div class="{base}"><span class="gcl">{lbl}</span>'
                f'<span class="gcv dim">—</span><span class="gcs">&nbsp;</span></div>')
    pct = hr[0] / hr[1] * 100
    cls = " hot" if pct >= 65 else (" cold" if pct <= 35 else "")   # hot/cold: no collision with .b bars
    return (f'<div class="{base}{cls}"><span class="gcl">{lbl}</span>'
            f'<span class="gcv">{pct:.0f}%</span><span class="gcs">{hr[0]}/{hr[1]}</span></div>')


def _book_prices(r):
    """[(book, dec_odds)] for THIS prop's bet side, BEST price first, from the last day of fd_lines
    (FanDuel + DraftKings live in the same table). Reads the freshest lines DB (resilient to a
    dropped collect-odds cron). Lets the card show WHERE the best number is. [] if unreachable."""
    import wnba_props_db as PDB
    db = PDB.props_db()
    if not Path(db).exists():
        return []
    side = (r.get("side") if hasattr(r, "get") else r["side"]) or "over"
    try:
        con = sqlite3.connect(f"file:{db}?mode=ro", uri=True)
        rows = con.execute(
            "SELECT COALESCE(book,'fd') b, odds, collected_at FROM fd_lines WHERE sport='wnba' "
            "AND player=? AND stat=? AND ROUND(line,1)=? AND side=? "
            "AND collected_at > datetime('now','-1 day')",
            (r["player"], r["stat"], round(float(r["line"]), 1), side)).fetchall()
        con.close()
    except Exception:
        return []
    if not rows:
        return []
    # CURRENT snapshot only: the LATEST quote per book, and drop a book whose newest quote for this
    # rung is stale (>FRESH_MIN behind the newest) — never MAX-over-time, which showed an old, more
    # generous price as the live 'best price' (the same bug fixed in posted_props).
    newest = max(ca for _, _, ca in rows)
    try:
        cutoff = (dt.datetime.fromisoformat(newest) - dt.timedelta(minutes=FRESH_MIN)).isoformat()
    except ValueError:
        cutoff = ""
    latest = {}                                        # book -> (collected_at, odds)
    for b, o, ca in rows:
        if o is None or ca < cutoff:
            continue
        if b not in latest or ca > latest[b][0]:
            latest[b] = (ca, o)
    return sorted([(b, o) for b, (ca, o) in latest.items()], key=lambda x: -x[1])


def _prop_row(r, rungs=None):
    """props.cash-style prop: side pill + line + stat, best-book odds + model edge on the right, the
    hit-rate HEATMAP (ROLE injury-context · L5 · L10 · season · H2H, green/red), and a compact
    context line (usage driver + matchup D + the runner-up book price). Taps to expand the bars."""
    stat = STAT.get(r["stat"], r["stat"].upper())
    # REB WATCH (2026-07-17, user call): reb-component bets are 7-10 / -3.1u live while everything
    # else prints — he personally skips them until the ~30-bet checkpoint; the system still flags,
    # grades and tracks them all. The tag tells him at a glance which plays are on his skip list.
